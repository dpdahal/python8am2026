# What is python?
# Python is a high-level, interpreted programming language known for its simplicity and readability.
#  It was created by Guido van Rossum and first released in 1991.

# Python is also an object-oriented programming language, it supports object-oriented programming (OOP) which allows developers to create and manipulate objects that represent real-world entities.

# Python is a versatile language that can be used for a wide range of applications, including web development, data analysis, machine learning, artificial intelligence, scientific computing, and more.

